package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		//syntax: initialization condition iteration
		//type for and ctrl+space
		//to print the numbers from 1 to 5
		
		
		for (int i = 1; i<=5; i++) {
			System.out.println(i);
			
		}
		System.out.println("-----------------");
		
		for (int i = 5; i>0; i--) {
			System.out.println(i);
		}

	}

}
